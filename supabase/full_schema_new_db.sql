-- Cherepovets89 / GOV site: базовая схема для нового Supabase-проекта.
-- Выполнять в Supabase -> SQL Editor -> New query.
-- Этот SQL создаёт таблицы сайта, VK-привязок и Vercel VK-бота.
-- Auth-пользователи создаются через Supabase Auth при регистрации на сайте; пароли старых пользователей SQL не переносит.

create extension if not exists pgcrypto;

-- Общий trigger для updated_at.
create or replace function public.ch89_touch_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

-- Основная таблица: отчёты, роли, настройки, служебные записи, баллы.
create table if not exists public.reports (
  id text primary key,
  email text not null default '',
  link text not null default '',
  date text not null default '',
  status text not null default '',
  xp numeric not null default 0,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists reports_email_idx on public.reports (email);
create index if not exists reports_link_idx on public.reports (link);
create index if not exists reports_status_idx on public.reports (status);
create index if not exists reports_email_status_idx on public.reports (email, status);
create index if not exists reports_email_link_idx on public.reports (email, link);
create index if not exists reports_xp_idx on public.reports (xp);

drop trigger if exists ch89_reports_touch_updated_at on public.reports;
create trigger ch89_reports_touch_updated_at
before update on public.reports
for each row execute function public.ch89_touch_updated_at();

-- Профили и статистика пользователей сайта.
create table if not exists public.user_stats (
  user_id text primary key,
  email text not null default '',
  nickname text not null default '',
  mod_correct integer not null default 0,
  sup_correct integer not null default 0,
  shop_spent numeric not null default 0,
  ap_spent numeric not null default 0,
  xp numeric not null default 0,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists user_stats_email_idx on public.user_stats (email);
create index if not exists user_stats_nickname_idx on public.user_stats (nickname);
create index if not exists user_stats_shop_spent_idx on public.user_stats (shop_spent);

drop trigger if exists ch89_user_stats_touch_updated_at on public.user_stats;
create trigger ch89_user_stats_touch_updated_at
before update on public.user_stats
for each row execute function public.ch89_touch_updated_at();

-- Логи магазина/выдачи товаров.
create table if not exists public.admin_logs (
  id bigint generated by default as identity primary key,
  user_email text not null default '',
  nickname text not null default '',
  item_name text not null default '',
  cost numeric not null default 0,
  type text not null default '',
  status text not null default 'Ожидает выдачи',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists admin_logs_user_email_idx on public.admin_logs (user_email);
create index if not exists admin_logs_type_idx on public.admin_logs (type);
create index if not exists admin_logs_status_idx on public.admin_logs (status);
create index if not exists admin_logs_created_at_idx on public.admin_logs (created_at desc);

drop trigger if exists ch89_admin_logs_touch_updated_at on public.admin_logs;
create trigger ch89_admin_logs_touch_updated_at
before update on public.admin_logs
for each row execute function public.ch89_touch_updated_at();

-- Привязка VK ID к аккаунту сайта.
create table if not exists public.vk_links (
  vk_user_id text primary key,
  site_user_id text not null,
  email text not null default '',
  nickname text not null default '',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create unique index if not exists vk_links_site_user_id_key on public.vk_links (site_user_id);
create index if not exists vk_links_email_idx on public.vk_links (email);

drop trigger if exists ch89_vk_links_touch_updated_at on public.vk_links;
create trigger ch89_vk_links_touch_updated_at
before update on public.vk_links
for each row execute function public.ch89_touch_updated_at();

-- Сессии формы /отчет для serverless-бота на Vercel.
create table if not exists public.vk_report_sessions (
  session_key text primary key,
  vk_user_id text not null,
  peer_id text not null,
  step text not null,
  data jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
