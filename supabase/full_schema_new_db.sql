create index if not exists vk_report_sessions_vk_user_id_idx on public.vk_report_sessions (vk_user_id);
create index if not exists vk_report_sessions_updated_at_idx on public.vk_report_sessions (updated_at);

drop trigger if exists ch89_vk_report_sessions_touch_updated_at on public.vk_report_sessions;
create trigger ch89_vk_report_sessions_touch_updated_at
before update on public.vk_report_sessions
for each row execute function public.ch89_touch_updated_at();

-- Права доступа.
-- Для совместимости со старым фронтендом основные таблицы открыты для клиента сайта.
-- Ограничение прав в этом проекте в основном сделано на уровне UI/логики сайта.
alter table public.reports disable row level security;
alter table public.user_stats disable row level security;
alter table public.admin_logs disable row level security;

grant usage on schema public to anon, authenticated, service_role;
grant select, insert, update, delete on public.reports to anon, authenticated, service_role;
grant select, insert, update, delete on public.user_stats to anon, authenticated, service_role;
grant select, insert, update, delete on public.admin_logs to anon, authenticated, service_role;
grant usage, select on all sequences in schema public to anon, authenticated, service_role;

-- vk_links доступна пользователю только для своей привязки. Бот с service_role обходит RLS.
alter table public.vk_links enable row level security;

drop policy if exists vk_links_select_own on public.vk_links;
create policy vk_links_select_own
on public.vk_links
for select
to authenticated
using (auth.uid()::text = site_user_id);

drop policy if exists vk_links_insert_own on public.vk_links;
create policy vk_links_insert_own
on public.vk_links
for insert
to authenticated
with check (auth.uid()::text = site_user_id);

drop policy if exists vk_links_update_own on public.vk_links;
create policy vk_links_update_own
on public.vk_links
for update
to authenticated
using (auth.uid()::text = site_user_id)
with check (auth.uid()::text = site_user_id);

drop policy if exists vk_links_delete_own on public.vk_links;
create policy vk_links_delete_own
on public.vk_links
for delete
to authenticated
using (auth.uid()::text = site_user_id);

grant select, insert, update, delete on public.vk_links to authenticated, service_role;

-- vk_report_sessions должна использоваться только ботом через service_role.
alter table public.vk_report_sessions enable row level security;
grant select, insert, update, delete on public.vk_report_sessions to service_role;

-- Чтобы PostgREST сразу увидел новые таблицы.
notify pgrst, 'reload schema';
